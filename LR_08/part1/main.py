# Тема: Подготовка данных и базовые алгоритмы

def main():
    # Пример данных для первой части
    raw_data = [10.5, 20.2, 15.8, 30.1, 25.4]
    
    print("Анализ входных данных (Часть 1):")
    
    # Базовая обработка: округление и фильтрация
    processed_data = [round(x) for x in raw_data if x > 15]
    
    print(f"Исходные значения: {raw_data}")
    print(f"Обработанные значения (больше 15 и округленные): {processed_data}")

if __name__ == "__main__":
    main()
