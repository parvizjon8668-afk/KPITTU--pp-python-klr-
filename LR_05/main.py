# Лабораторная работа №5
# Тема: Словари и множества

def main():
    # 1. Работа со словарем (данные о студенте)
    # Ключ - ID, значение - имя студента
    students = {
        101: "Арипов",
        102: "Иванов",
        103: "Петров"
    }
    
    print(f"Список студентов (словарь): {students}")

    # Добавление нового элемента
    students[104] = "Сидоров"
    
    # 2. Работа с множествами (уникальные дисциплины)
    # Множества автоматически удаляют дубликаты
    courses = {"Программирование", "Математика", "Физика", "Программирование"}
    
    print(f"Уникальные дисциплины (множество): {courses}")

    # 3. Поиск в словаре по ключу
    student_id = 101
    if student_id in students:
        print(f"Студент с ID {student_id}: {students[student_id]}")

    # 4. Демонстрация методов
    print(f"Все ID студентов: {list(students.keys())}")
    print(f"Все имена: {list(students.values())}")

if __name__ == "__main__":
    main()
