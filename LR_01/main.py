# Поиск максимального элемента списка
# Практикум по программированию

# Исходный список чисел
numbers = [3, 7, 2, 9, 5]

# Предполагаем, что первый элемент — максимальный
max_number = numbers[0]

# Проходим по всем элементам списка
for num in numbers:
    # Если нашли число больше текущего максимума
    if num > max_number:
        max_number = num

# Вывод результата
print("Максимальный элемент списка:", max_number)
