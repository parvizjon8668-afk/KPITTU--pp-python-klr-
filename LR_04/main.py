# Тема: Работа со списками и кортежами

def main():
    # Создаем список целых чисел (пример данных из вашей работы)
    numbers = [10, 25, 4, 12, 8, 33, 1]
    
    print(f"Исходный список: {numbers}")

    # 1. Поиск минимального и максимального элементов
    min_val = min(numbers)
    max_val = max(numbers)
    print(f"Минимальное значение: {min_val}")
    print(f"Максимальное значение: {max_val}")

    # 2. Сортировка списка по возрастанию
    numbers.sort()
    print(f"Отсортированный список: {numbers}")

    # 3. Пример работы с кортежем (неизменяемый список)
    # Кортежи часто используются для защиты данных от изменений
    numbers_tuple = tuple(numbers)
    print(f"Список, преобразованный в кортеж: {numbers_tuple}")

    # 4. Вычисление суммы всех элементов
    total_sum = sum(numbers)
    print(f"Сумма всех элементов: {total_sum}")

if __name__ == "__main__":
    main()
